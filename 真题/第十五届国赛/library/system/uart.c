	TR1 = 1;			//魹ｽ魹ｽﾊｱ魹ｽ魹ｽ1魹ｽ魹ｽﾊｼ魹ｽ魹ｽﾊｱ
    ES = 1;                     //ﾊｹ魹ｽﾜｴ魹ｽ魹ｽ魹ｽ魹ｽﾐｶ魹ｽ
    EA = 1;	
