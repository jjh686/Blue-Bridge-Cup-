	AUXR &= 0xFE;		//魹ｽ魹ｽ魹ｽ魹ｽ1ﾑ｡魹ｽ魹ｽﾊｱ魹ｽ魹ｽ1ﾎｪ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
	TMOD &= 0x0F;		//魹ｽ魹ｽ魹ｽﾃｶ魹ｽﾊｱ魹ｽ魹ｽﾄ｣ﾊｽ
	TL1 = 0xC7;			//魹ｽ魹ｽ魹ｽﾃｶ魹ｽﾊｱ魹ｽ魹ｽﾊｼﾖｵ
	TH1 = 0xFE;			//魹ｽ魹ｽ魹ｽﾃｶ魹ｽﾊｱ魹ｽ魹ｽﾊｼﾖｵ
